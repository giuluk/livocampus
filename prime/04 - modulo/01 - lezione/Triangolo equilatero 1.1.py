import turtle

# Assegnazioni delle variabili
tom = turtle.Turtle()
lato = 150

# Attributi della tartaruga
tom.shape('turtle')

# Azioni della tartaruga
tom.forward(lato)
tom.left(120)
        
tom.forward(lato)
tom.left(120)

tom.forward(lato)
tom.left(120)